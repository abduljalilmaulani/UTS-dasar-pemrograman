#include <iostream>
using namespace std;

int main()
{
    int i, j;
         i = 5;
         cout << "Nilai i Awal : " << i <<endl;
         cout << "Nilai ++1    : " << ++i <<endl;
         cout << "Nilai i Akhir: " << i <<endl<<endl;

         j = 10;
         cout << "Nilai j Awal : " << j <<endl;
         cout << "Nilai j++    : " << j++ <<endl;
         cout << "Nilai j Akhir: " << j <<endl;

         return 0;
}
